uint16_t crc16_calculate_modbus(uint8_t *p_data, uint16_t len)
{
	uint8_t crchi = 0xff;
	uint8_t crclo = 0xff;
	uint16_t index;
  len -= 2;
	while (len--)
	{
		index = crclo ^ *p_data++;
		crclo = crchi ^ a_crc16mTableHi[index];
		crchi = a_crc16mTableLo[index];
	}
	return (crchi << 8 | crclo);
}
